#include<iostream>
using namespace std;
int main()
{
	int a;
	char b;
	float c;
	double d;
	cout<<"Size of char: "<<sizeof(b)<<" byte"<<endl;
	cout<<"Size of int: "<<sizeof(a)<<" bytes"<<endl;
	cout<<"Size of float: "<<sizeof(c)<<" bytes"<<endl;
	cout<<"Size of double: "<<sizeof(d)<<" bytes"<<endl;
	return 0;
}
